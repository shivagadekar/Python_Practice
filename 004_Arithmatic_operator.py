# Python Arithmetic Operators

x = 21
y = 5
z = 3

addition = x + y  # Addition operator
print(addition)

subtraction = x - y  # Subtraction Operator
print(subtraction)

multiplication = x * y  # Multiplication Operator
print(multiplication)

division = x / y  # Division Operator
print(division)

reminder = x % y  # Always Giver reminder
print(reminder)

power = x ** 2.5  # Y times X
print(power)

quotient = x // y  # Giver perfect number
print(quotient)
