x = 21
y = 5
z = 21

# Comparison operator gives output in form of 'True or False' Only.
# It is mostly used in while loop, in if else statements.
# = Assign
# == is equal to

print(x == y)  # False
print(x == z)  # True

print(x != y)  # True
print(x != z)  # False

print(x > y)  # True
print(x > z)  # False

print(x < y)  # False
print(x < z)  # False

print(x >= y)  # True
print(x >= z)  # True

print(x <= y)  # False
print(x <= z)  # True

print(x >= y and x >= z)  # True (Both need to satisfy)

print(x > y or x > z)  # True (Only One need to Satisfy)

print(not False)  # True  (Opposite of Given Condition), Used in while loop

print(14 >= 14)  # True
print(14 > 14)  # False
print(20 >= 16)  # True

print(14 <= 14)  # True
